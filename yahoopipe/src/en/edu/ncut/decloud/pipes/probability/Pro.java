package en.edu.ncut.decloud.pipes.probability;

public class Pro {

	private String type;
	private double inPro;
	private double outPro;
	private double conPro;

	public String getType() {
		return type;
	}

	public void setType(String type) {
		this.type = type;
	}

	public double getInPro() {
		return inPro;
	}

	public void setInPro(double inPro) {
		this.inPro = inPro;
	}

	public double getOutPro() {
		return outPro;
	}

	public void setOutPro(double outPro) {
		this.outPro = outPro;
	}

	public double getConPro() {
		return conPro;
	}

	public void setConPro(double conPro) {
		this.conPro = conPro;
	}
}
